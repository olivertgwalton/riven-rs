//! Generates the per-plugin documentation under `docs/plugins/`, plus the
//! environment-variable inventory in `docs/environment.md`.
//!
//! The settings tables are derived from each plugin's `settings_schema()`, so
//! they cannot drift from the code the way hand-maintained tables do. The
//! environment inventory is derived the same way, except its source is the
//! Rust sources themselves: process tuning knobs are read straight from the
//! environment and never appear in any schema, so a curated list would silently
//! omit whatever someone added last. Scanning for the reads finds them all. Run:
//!
//! ```text
//! cargo run --bin gen-docs
//! ```
//!
//! Each plugin page is part hand-written, part generated. Everything between
//! the `BEGIN GENERATED` / `END GENERATED` markers is rewritten on every run;
//! everything outside them is left untouched, so prose added to a page
//! survives regeneration. Pages that do not exist yet are created with a
//! placeholder header for someone to fill in.

use std::collections::BTreeMap;
use std::fmt::Write as _;
use std::fs;
use std::path::{Path, PathBuf};

use riven_core::plugin::{FieldType, Plugin, SettingField};
use riven_plugins::all_plugins;

const BEGIN_MARKER: &str = "<!-- BEGIN GENERATED -->";
const END_MARKER: &str = "<!-- END GENERATED -->";
const DO_NOT_EDIT: &str =
    "<!-- Generated by `cargo run --bin gen-docs`. Do not edit this block by hand. -->";

/// Category key -> heading used on the index page. Categories not listed here
/// fall through to a trailing "Other" section.
const CATEGORY_LABELS: &[(&str, &str)] = &[
    ("sources", "Sources"),
    ("media", "Media servers"),
    ("services", "Services"),
];

fn main() -> anyhow::Result<()> {
    // `--check` reports what would change and exits non-zero instead of
    // writing, so CI can fail on docs that have fallen behind the schemas.
    let check_only = std::env::args().any(|arg| arg == "--check");

    let root = repo_root();
    let out_dir = root.join("docs/plugins");
    if !check_only {
        fs::create_dir_all(&out_dir)?;
    }

    let plugins = all_plugins();
    let mut stale = Vec::new();

    for plugin in &plugins {
        let path = out_dir.join(format!("{}.md", plugin.name()));
        let body = render_plugin(plugin.as_ref())?;
        if write_generated_block(&path, &body, check_only, || plugin_stub(plugin.as_ref()))? {
            stale.push(path);
        }
    }

    let index_path = out_dir.join("README.md");
    let index_body = render_index(&plugins)?;
    if write_generated_block(&index_path, &index_body, check_only, index_stub)? {
        stale.push(index_path);
    }

    let env_vars = scan_env_vars(&root.join("crates"), &root)?;
    let env_path = root.join("docs/environment.md");
    let env_body = render_env(&env_vars)?;
    if write_generated_block(&env_path, &env_body, check_only, env_stub)? {
        stale.push(env_path);
    }

    if check_only && !stale.is_empty() {
        eprintln!("generated docs are out of date. Run `make docs` and commit the result:");
        for path in &stale {
            eprintln!("  {}", path.display());
        }
        anyhow::bail!("{} page(s) out of date", stale.len());
    }

    println!(
        "gen-docs: {} plugin pages, {} environment variables, {} file(s) {} ({})",
        plugins.len(),
        env_vars.len(),
        stale.len(),
        if check_only { "stale" } else { "changed" },
        root.join("docs").display()
    );
    Ok(())
}

// ---------------------------------------------------------------------------
// Environment-variable discovery
// ---------------------------------------------------------------------------

/// Where one variable is read, as a repo-relative `path:line`.
type Site = String;

/// Finds every environment variable the code reads, by scanning the sources
/// under `crates_dir`. Two patterns are recognised:
///
/// 1. A literal passed straight to `env::var` / `env::var_os`. Unambiguous —
///    whatever the name looks like, that is an environment read.
/// 2. Any `RIVEN_*`-shaped string literal. Reads are often indirected through
///    a helper (`env_positive("RIVEN_USENET_CACHE_BYTES", DEFAULT)`), which
///    puts the name and the `env::var` call in different functions, so pattern
///    1 alone would miss most of Riven's own knobs.
///
/// Deliberately textual rather than syntactic: it has no build step, it cannot
/// go stale against a refactor, and over-reporting a name that turns out not to
/// be an environment variable is a far cheaper failure than silently omitting
/// one that is.
fn scan_env_vars(crates_dir: &Path, root: &Path) -> anyhow::Result<BTreeMap<String, Vec<Site>>> {
    let mut found: BTreeMap<String, Vec<Site>> = BTreeMap::new();
    let mut sources = Vec::new();
    collect_rust_sources(crates_dir, &mut sources)?;
    sources.sort();

    for path in sources {
        let relative = path
            .strip_prefix(root)
            .unwrap_or(&path)
            .to_string_lossy()
            .replace('\\', "/");
        // This file's own patterns and examples are descriptions of reads, not
        // reads. `file!()` keeps the exclusion correct if the binary moves.
        if relative == file!().replace('\\', "/") {
            continue;
        }
        let contents = fs::read_to_string(&path)?;
        for (index, line) in contents.lines().enumerate() {
            for name in env_names_in_line(line) {
                found
                    .entry(name)
                    .or_default()
                    .push(format!("{relative}:{}", index + 1));
            }
        }
    }
    Ok(found)
}

fn collect_rust_sources(dir: &Path, out: &mut Vec<PathBuf>) -> anyhow::Result<()> {
    for entry in fs::read_dir(dir)? {
        let path = entry?.path();
        let name = path.file_name().unwrap_or_default().to_string_lossy();
        if path.is_dir() {
            // `target` holds generated code and vendored sources; scanning it
            // would report names this repo does not own.
            if name != "target" && name != "node_modules" && !name.starts_with('.') {
                collect_rust_sources(&path, out)?;
            }
        } else if path.extension().is_some_and(|ext| ext == "rs") {
            out.push(path);
        }
    }
    Ok(())
}

/// Environment-variable names referenced on one line of Rust source.
fn env_names_in_line(line: &str) -> Vec<String> {
    let mut names = Vec::new();

    // Pattern 1: a literal argument to env::var / env::var_os. Note this can
    // never match `env!(..)`, which is a compile-time lookup, not a read.
    let mut cursor = 0;
    while let Some(offset) = line[cursor..].find("env::var") {
        let after = cursor + offset + "env::var".len();
        let rest = line[after..].trim_start_matches("_os");
        if let Some(inner) = rest.strip_prefix('(')
            && let Some(name) = leading_string_literal(inner.trim_start())
            && is_env_name(name)
        {
            names.push(name.to_string());
        }
        cursor = after;
    }

    // Pattern 2: a RIVEN_-shaped literal anywhere, catching helper indirection.
    for literal in string_literals(line) {
        if literal.starts_with("RIVEN_") && is_env_name(literal) && !names.contains(&literal.into())
        {
            names.push(literal.to_string());
        }
    }

    names
}

/// The contents of a double-quoted literal starting at `text`, if any.
fn leading_string_literal(text: &str) -> Option<&str> {
    let rest = text.strip_prefix('"')?;
    let end = rest.find('"')?;
    rest.get(..end)
}

/// Every double-quoted literal on a line. Escapes are not interpreted — a name
/// containing one would fail [`is_env_name`] anyway.
fn string_literals(line: &str) -> Vec<&str> {
    let mut out = Vec::new();
    let mut rest = line;
    while let Some(start) = rest.find('"') {
        let after = &rest[start + 1..];
        let Some(end) = after.find('"') else {
            break;
        };
        if let Some(literal) = after.get(..end) {
            out.push(literal);
        }
        rest = &after[end + 1..];
    }
    out
}

/// Shaped like an environment variable: upper-case, digits and underscores.
/// Excludes format strings and prose that happen to sit next to a read.
fn is_env_name(candidate: &str) -> bool {
    !candidate.is_empty()
        && candidate
            .chars()
            .all(|c| c.is_ascii_uppercase() || c.is_ascii_digit() || c == '_')
}

fn render_env(vars: &BTreeMap<String, Vec<Site>>) -> anyhow::Result<String> {
    // Group by the crate the variable is read from, so the table reads as a
    // map of the system rather than one flat alphabetical list.
    let mut by_crate: BTreeMap<&str, Vec<(&String, &Vec<Site>)>> = BTreeMap::new();
    for (name, sites) in vars {
        let owner = sites
            .first()
            .and_then(|site| site.split('/').nth(1))
            .unwrap_or("other");
        by_crate.entry(owner).or_default().push((name, sites));
    }

    let mut out = String::new();
    for (crate_name, entries) in &by_crate {
        writeln!(out, "### `{crate_name}`\n")?;
        out.push_str("| Variable | Kind | Read at |\n");
        out.push_str("| --- | --- | --- |\n");
        for (name, sites) in entries {
            // A trailing separator means the code builds the full name at
            // runtime; the row documents the family, not one variable.
            let kind = if name.ends_with('_') {
                "prefix"
            } else {
                "variable"
            };
            let read_at = sites
                .iter()
                .map(|site| format!("`{site}`"))
                .collect::<Vec<_>>()
                .join("<br>");
            writeln!(out, "| `{name}` | {kind} | {read_at} |")?;
        }
        out.push('\n');
    }
    Ok(out)
}

/// `CARGO_MANIFEST_DIR` is `crates/riven-plugins`; the repo root is two up.
fn repo_root() -> PathBuf {
    Path::new(env!("CARGO_MANIFEST_DIR"))
        .ancestors()
        .nth(2)
        .expect("manifest dir is always crates/riven-plugins")
        .to_path_buf()
}

