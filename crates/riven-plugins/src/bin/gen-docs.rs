//! Generates the per-plugin documentation under `docs/plugins/`.
//!
//! The settings tables are derived from each plugin's `settings_schema()`, so
//! they cannot drift from the code the way hand-maintained tables do. Run:
//!
//! ```text
//! cargo run --bin gen-docs
//! ```
//!
//! Each plugin page is part hand-written, part generated. Everything between
//! the `BEGIN GENERATED` / `END GENERATED` markers is rewritten on every run;
//! everything outside them is left untouched, so prose added to a page
//! survives regeneration. Pages that do not exist yet are created with a
//! placeholder header for someone to fill in.

use std::collections::BTreeMap;
use std::fmt::Write as _;
use std::fs;
use std::path::{Path, PathBuf};

use riven_core::plugin::{FieldType, Plugin, SettingField};
use riven_plugins::all_plugins;

const BEGIN_MARKER: &str = "<!-- BEGIN GENERATED -->";
const END_MARKER: &str = "<!-- END GENERATED -->";
const DO_NOT_EDIT: &str = "<!-- Generated by `cargo run --bin gen-docs` from each plugin's `settings_schema()`. Do not edit this block by hand. -->";

/// Category key -> heading used on the index page. Categories not listed here
/// fall through to a trailing "Other" section.
const CATEGORY_LABELS: &[(&str, &str)] = &[
    ("sources", "Sources"),
    ("media", "Media servers"),
    ("services", "Services"),
];

fn main() -> anyhow::Result<()> {
    // `--check` reports what would change and exits non-zero instead of
    // writing, so CI can fail on docs that have fallen behind the schemas.
    let check_only = std::env::args().any(|arg| arg == "--check");

    let root = repo_root();
    let out_dir = root.join("docs/plugins");
    if !check_only {
        fs::create_dir_all(&out_dir)?;
    }

    let plugins = all_plugins();
    let mut stale = Vec::new();

    for plugin in &plugins {
        let path = out_dir.join(format!("{}.md", plugin.name()));
        let body = render_plugin(plugin.as_ref())?;
        if write_generated_block(&path, &body, check_only, || plugin_stub(plugin.as_ref()))? {
            stale.push(path);
        }
    }

    let index_path = out_dir.join("README.md");
    let index_body = render_index(&plugins)?;
    if write_generated_block(&index_path, &index_body, check_only, index_stub)? {
        stale.push(index_path);
    }

    if check_only && !stale.is_empty() {
        eprintln!("docs/plugins is out of date. Run `make docs` and commit the result:");
        for path in &stale {
            eprintln!("  {}", path.display());
        }
        anyhow::bail!("{} page(s) out of date", stale.len());
    }

    println!(
        "gen-docs: {} plugin pages, {} file(s) {} ({})",
        plugins.len(),
        stale.len(),
        if check_only { "stale" } else { "changed" },
        out_dir.display()
    );
    Ok(())
}

/// `CARGO_MANIFEST_DIR` is `crates/riven-plugins`; the repo root is two up.
fn repo_root() -> PathBuf {
    Path::new(env!("CARGO_MANIFEST_DIR"))
        .ancestors()
        .nth(2)
        .expect("manifest dir is always crates/riven-plugins")
        .to_path_buf()
}

// ---------------------------------------------------------------------------
// Rendering
// ---------------------------------------------------------------------------

fn render_plugin(plugin: &dyn Plugin) -> anyhow::Result<String> {
    let name = plugin.name();
    let mut out = String::new();

    let category = CATEGORY_LABELS
        .iter()
        .find(|(key, _)| *key == plugin.category())
        .map_or("Other", |(_, label)| *label);
    writeln!(out, "**Category:** {category}\n")?;

    let events = plugin.subscribed_events();
    if events.is_empty() {
        out.push_str("This plugin subscribes to no events; it is driven directly by the core.\n\n");
    } else {
        out.push_str("### Events\n\nSubscribes to:\n\n");
        for event in events {
            writeln!(out, "- `{}`", event.slug())?;
        }
        out.push('\n');
    }

    out.push_str("### Settings\n\n");
    let schema = plugin.settings_schema();
    if schema.is_empty() {
        out.push_str("This plugin has no configurable settings.\n");
        return Ok(out);
    }

    let prefix = format!("RIVEN_PLUGIN_SETTING__{}__", name.to_uppercase());
    writeln!(
        out,
        "Set via environment variables prefixed with `{prefix}`, or in the settings UI.\n"
    )?;

    // Fields may be grouped into sections; render each group under its own
    // heading and keep the ungrouped ones first, matching how the UI lays them out.
    let mut ungrouped: Vec<&SettingField> = Vec::new();
    let mut sections: BTreeMap<&str, Vec<&SettingField>> = BTreeMap::new();
    for field in &schema {
        match field.section.as_deref() {
            Some(section) => sections.entry(section).or_default().push(field),
            None => ungrouped.push(field),
        }
    }

    if !ungrouped.is_empty() {
        render_table(&mut out, &prefix, &ungrouped)?;
    }
    for (section, fields) in &sections {
        writeln!(out, "#### {section}\n")?;
        render_table(&mut out, &prefix, fields)?;
    }

    // Container fields carry their own child schemas; document those after the
    // top-level table so the parent table stays readable.
    for field in &schema {
        render_nested(&mut out, &field.key, field)?;
    }

    Ok(out)
}

fn render_table(out: &mut String, prefix: &str, fields: &[&SettingField]) -> anyhow::Result<()> {
    out.push_str("| Setting | Environment variable | Type | Required | Default | Description |\n");
    out.push_str("| --- | --- | --- | --- | --- | --- |\n");
    for field in fields {
        writeln!(
            out,
            "| `{key}` | `{prefix}{key}` | {ty} | {required} | {default} | {description} |",
            key = field.key,
            ty = field_type_label(field),
            required = if field.required { "Yes" } else { "No" },
            default = field
                .default_value
                .as_deref()
                .map_or_else(|| "—".to_string(), |v| format!("`{}`", escape_cell(v))),
            description = field
                .description
                .as_deref()
                .map_or("—", |d| d)
                .replace('|', "\\|"),
        )?;
    }
    out.push('\n');
    Ok(())
}

/// Emits a sub-table for each container field's children, recursing so deeply
/// nested schemas (object of objects, dictionaries of objects) are all covered.
fn render_nested(out: &mut String, path: &str, field: &SettingField) -> anyhow::Result<()> {
    let children = field
        .fields
        .as_deref()
        .or(field.item_fields.as_deref())
        .unwrap_or_default();
    if children.is_empty() {
        return Ok(());
    }

    let noun = if field.fields.is_some() {
        "Fields"
    } else {
        "Fields of each entry"
    };
    writeln!(out, "#### `{path}`\n")?;
    if let Some(description) = &field.description {
        writeln!(out, "{description}\n")?;
    }
    writeln!(out, "{noun}:\n")?;
    out.push_str("| Field | Type | Required | Default | Description |\n");
    out.push_str("| --- | --- | --- | --- | --- |\n");
    for child in children {
        writeln!(
            out,
            "| `{key}` | {ty} | {required} | {default} | {description} |",
            key = child.key,
            ty = field_type_label(child),
            required = if child.required { "Yes" } else { "No" },
            default = child
                .default_value
                .as_deref()
                .map_or_else(|| "—".to_string(), |v| format!("`{}`", escape_cell(v))),
            description = child
                .description
                .as_deref()
                .map_or("—", |d| d)
                .replace('|', "\\|"),
        )?;
    }
    out.push('\n');

    for child in children {
        render_nested(out, &format!("{path}.{}", child.key), child)?;
    }
    Ok(())
}

fn field_type_label(field: &SettingField) -> String {
    let base = match field.field_type {
        FieldType::Text => "text",
        FieldType::Password => "secret",
        FieldType::Url => "url",
        FieldType::Number => "number",
        FieldType::Boolean => "boolean",
        FieldType::Textarea => "text (multiline)",
        FieldType::Select => "select",
        FieldType::Object => "object",
        FieldType::Dictionary => "dictionary",
        FieldType::StringArray => "string list",
        FieldType::NullableBoolean => "boolean (optional)",
        FieldType::CustomRank => "custom rank",
    };
    match &field.options {
        Some(options) if !options.is_empty() => {
            let joined = options
                .iter()
                .map(|o| format!("`{o}`"))
                .collect::<Vec<_>>()
                .join(", ");
            format!("{base} ({joined})")
        }
        _ => base.to_string(),
    }
}

/// Table cells are pipe-delimited and newlines would break the row.
fn escape_cell(value: &str) -> String {
    value.replace('|', "\\|").replace('\n', " ")
}

fn render_index(plugins: &[Box<dyn Plugin>]) -> anyhow::Result<String> {
    let mut by_category: BTreeMap<&str, Vec<&dyn Plugin>> = BTreeMap::new();
    for plugin in plugins {
        by_category
            .entry(plugin.category())
            .or_default()
            .push(plugin.as_ref());
    }

    let mut out = String::new();
    for (key, label) in CATEGORY_LABELS {
        if let Some(plugins) = by_category.remove(*key) {
            render_index_section(&mut out, label, &plugins)?;
        }
    }
    // Anything with a category not in CATEGORY_LABELS still gets listed.
    let leftovers: Vec<&dyn Plugin> = by_category.into_values().flatten().collect();
    if !leftovers.is_empty() {
        render_index_section(&mut out, "Other", &leftovers)?;
    }

    Ok(out)
}

fn render_index_section(
    out: &mut String,
    label: &str,
    plugins: &[&dyn Plugin],
) -> anyhow::Result<()> {
    writeln!(out, "### {label}\n")?;
    writeln!(out, "| Plugin | Settings | Required settings |")?;
    writeln!(out, "| --- | --- | --- |")?;
    for plugin in plugins {
        let name = plugin.name();
        let schema = plugin.settings_schema();
        let required = schema
            .iter()
            .filter(|field| field.required)
            .map(|field| format!("`{}`", field.key))
            .collect::<Vec<_>>();
        writeln!(
            out,
            "| [{name}]({name}.md) | {count} | {required} |",
            count = schema.len(),
            required = if required.is_empty() {
                "—".to_string()
            } else {
                required.join(", ")
            }
        )?;
    }
    out.push('\n');
    Ok(())
}

// ---------------------------------------------------------------------------
// Marker-preserving file writes
// ---------------------------------------------------------------------------

/// Replaces the generated block in `path`, keeping hand-written content around
/// it. Creates the file from `stub` when it does not exist. Returns whether the
/// file needed updating — when `check_only`, nothing is written and the return
/// value just reports whether it is stale.
fn write_generated_block(
    path: &Path,
    body: &str,
    check_only: bool,
    stub: impl FnOnce() -> String,
) -> anyhow::Result<bool> {
    let block = format!(
        "{BEGIN_MARKER}\n{DO_NOT_EDIT}\n\n{}\n{END_MARKER}\n",
        body.trim_end()
    );

    let existing = fs::read_to_string(path).ok();
    let updated = match &existing {
        Some(current) => splice(current, &block).ok_or_else(|| {
            anyhow::anyhow!(
                "{} has a {BEGIN_MARKER} without a matching {END_MARKER}",
                path.display()
            )
        })?,
        None => stub().replace(BEGIN_MARKER, &block),
    };

    if existing.as_deref() == Some(updated.as_str()) {
        return Ok(false);
    }
    if !check_only {
        fs::write(path, updated)?;
    }
    Ok(true)
}

/// Swaps the marker-delimited region of `current` for `block`. Appends the
/// block when the file has no markers yet. `None` means unbalanced markers.
fn splice(current: &str, block: &str) -> Option<String> {
    let Some(start) = current.find(BEGIN_MARKER) else {
        return Some(if current.ends_with("\n\n") {
            format!("{current}{block}")
        } else if current.ends_with('\n') {
            format!("{current}\n{block}")
        } else {
            format!("{current}\n\n{block}")
        });
    };
    let end = current.find(END_MARKER)? + END_MARKER.len();
    if end < start {
        return None;
    }
    let trailing = current[end..].strip_prefix('\n').unwrap_or(&current[end..]);
    Some(format!("{}{block}{trailing}", &current[..start]))
}

fn plugin_stub(plugin: &dyn Plugin) -> String {
    let name = plugin.name();
    format!(
        "# {name}\n\n\
         <!-- Add an overview of what this plugin does, what you need to set it up,\n\
              and any gotchas. Anything outside the generated block below is kept\n\
              across runs of `cargo run --bin gen-docs`. -->\n\n\
         {BEGIN_MARKER}\n"
    )
}

fn index_stub() -> String {
    format!(
        "# Plugins\n\n\
         Riven's integrations are plugins. Each one subscribes to core events and is\n\
         configured independently — a plugin with unmet required settings is skipped\n\
         rather than failing startup.\n\n\
         Settings are read from environment variables named\n\
         `RIVEN_PLUGIN_SETTING__<PLUGIN>__<setting>`, where `<PLUGIN>` is the plugin\n\
         name in uppercase. Values stored through the settings UI are layered on top\n\
         of the environment.\n\n\
         ```bash\n\
         RIVEN_PLUGIN_SETTING__TMDB__api_key=\"your-tmdb-key\"\n\
         ```\n\n\
         {BEGIN_MARKER}\n"
    )
}
